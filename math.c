#include<stdio.h>
#include<math.h>
void main()
{
	printf("\n ceil value : %.2f",ceil(3.2));
	printf("\n ceil value : %.2f",ceil(3.9));
	
	printf("\n floor value : %.2f",floor(3.2));
	printf("\n floor value : %.2f",floor(3.9));
	
	printf("\n sqrt value : %.2f",sqrt(16));
	printf("\n sqrt value : %.2f",sqrt(7));
	
	printf("\n pow value : %.2f",pow(2,4));
	printf("\n pow value : %.2f",pow(2.2,1.3));
	
	printf("\n abs value : %.2f",abs(-15));
	
	getch();
	
	
}
